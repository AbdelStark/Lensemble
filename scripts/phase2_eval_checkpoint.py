#!/usr/bin/env python3
"""Generate a Phase 2 downstream EvalReport artifact from a public checkpoint."""

from __future__ import annotations

import argparse
import importlib
import json
import shlex
from pathlib import Path
from typing import Literal, cast

from lensemble.eval import (
    Phase2CheckpointRef,
    build_phase2_downstream_eval_report,
    parse_phase2_downstream_eval_report,
    phase2_eval_config_from_checkpoint,
)

_PLANNERS = ("cem", "icem", "mppi")
PlannerName = Literal["cem", "icem", "mppi"]
_DEFAULT_CLAIM_BOUNDARY = (
    "Compact engineering-scale downstream eval of a published Phase 2 checkpoint "
    "on the built-in synthetic://toy harness. It verifies checkpoint load, latent "
    "MPC execution, planner-cost reporting, and residency-safe EvalReport emission; "
    "it does not claim SO-100 task success or paper-scale LeWorldModel performance."
)
_DEFAULT_HELD_OUT_POLICY = (
    "synthetic://toy is deterministic and not drawn from the SO-100 training silos; "
    "the harness evaluates the configured --num-episodes seed-pinned episodes "
    "derived from root_seed."
)
_DEFAULT_GOAL_POLICY = (
    "fixed synthetic goal clip generated by the built-in synthetic://toy world "
    "with seed 7919; no raw goal observation is serialized."
)
_DEFAULT_ACTION_CLIPPING = (
    "none in the current Planner path; sampled Gaussian actions are passed to the "
    "fresh local ActionHead, while ActionSpec bounds are recorded for audit."
)


def _args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--checkpoint-dir",
        type=Path,
        help="Local checkpoint artifact dir. Omit to download from --checkpoint-repo/--checkpoint-revision.",
    )
    parser.add_argument("--output", type=Path)
    parser.add_argument(
        "--expected-checkpoint-hash",
        default="8f1494fd9e57b7496daf96e379a3de1457a435080b81b9e0ea1d20a52f4827c4",
    )
    parser.add_argument(
        "--checkpoint-repo", default="abdelstark/lensemble-phase2-so100-checkpoint"
    )
    parser.add_argument(
        "--checkpoint-revision", default="da52ef380ac87317c89e87f048d65bae65c16b9e"
    )
    parser.add_argument("--checkpoint-artifact-path", default="artifacts/round-00003")
    parser.add_argument(
        "--download-dir", type=Path, default=Path("/tmp/lensemble-phase2-checkpoint")
    )
    parser.add_argument("--training-job-id", default="6a22ba68e6aa50b87b9ebef7")
    parser.add_argument(
        "--training-job-url",
        default="https://huggingface.co/jobs/abdelstark/6a22ba68e6aa50b87b9ebef7",
    )
    parser.add_argument(
        "--code-sha", default="4b446a558882f25e47ee6410a4c32982bbf33477"
    )
    parser.add_argument(
        "--train-config-hash",
        default="82296109ed452a1aaf494306b100bd4f2b7e3b968a8d4219bc89a3461b0294a3",
    )
    parser.add_argument(
        "--source-report-uri",
        default="hf://models/abdelstark/lensemble-phase2-so100-checkpoint/claim_mvp_report.json",
    )
    parser.add_argument("--env-id", default="synthetic://toy")
    parser.add_argument("--planner", choices=_PLANNERS, default="icem")
    parser.add_argument("--planning-samples", type=int, default=8)
    parser.add_argument("--horizon", type=int, default=2)
    parser.add_argument("--planner-iters", type=int, default=4)
    parser.add_argument(
        "--num-episodes",
        type=int,
        default=4,
        help="Held-out episodes to evaluate. Must be at least 2 because effective_dim uses covariance.",
    )
    parser.add_argument("--root-seed", type=int, default=0)
    parser.add_argument(
        "--task-scale",
        default="synthetic downstream harness over a published Phase 2 checkpoint",
    )
    parser.add_argument("--held-out-policy", default=_DEFAULT_HELD_OUT_POLICY)
    parser.add_argument("--goal-policy", default=_DEFAULT_GOAL_POLICY)
    parser.add_argument("--action-clipping", default=_DEFAULT_ACTION_CLIPPING)
    parser.add_argument("--claim-boundary", default=_DEFAULT_CLAIM_BOUNDARY)
    parser.add_argument(
        "--eval-command",
        help="Override the reproducibility command recorded in the report.",
    )
    parser.add_argument(
        "--push", action="store_true", help="Upload the JSON report to the Hub."
    )
    parser.add_argument(
        "--out-repo",
        default="abdelstark/lensemble-phase2-so100-checkpoint",
        help="Hub repo receiving the report when --push is set.",
    )
    parser.add_argument(
        "--out-repo-type", choices=("model", "dataset"), default="model"
    )
    parser.add_argument(
        "--out-path",
        default="reports/phase2_downstream_eval_report.json",
        help="Path in --out-repo receiving the report when --push is set.",
    )
    return parser.parse_args()


def _canonical_command(args: argparse.Namespace) -> str:
    pieces = [
        "uv",
        "run",
        "--extra",
        "dev",
        "python",
        "scripts/phase2_eval_checkpoint.py",
        "--checkpoint-repo",
        str(args.checkpoint_repo),
        "--checkpoint-revision",
        str(args.checkpoint_revision),
        "--checkpoint-artifact-path",
        str(args.checkpoint_artifact_path),
        "--download-dir",
        str(args.download_dir),
        "--output",
        str(args.output or "phase2_downstream_eval_report.json"),
        "--expected-checkpoint-hash",
        str(args.expected_checkpoint_hash),
        "--env-id",
        str(args.env_id),
        "--planner",
        str(args.planner),
        "--planning-samples",
        str(args.planning_samples),
        "--horizon",
        str(args.horizon),
        "--planner-iters",
        str(args.planner_iters),
        "--num-episodes",
        str(args.num_episodes),
        "--root-seed",
        str(args.root_seed),
    ]
    if args.checkpoint_dir is not None:
        pieces.extend(["--checkpoint-dir", str(args.checkpoint_dir)])
    if args.push:
        pieces.extend(
            [
                "--push",
                "--out-repo",
                str(args.out_repo),
                "--out-repo-type",
                str(args.out_repo_type),
                "--out-path",
                str(args.out_path),
            ]
        )
    return shlex.join(pieces)


def main() -> None:
    args = _args()
    checkpoint_dir = _resolve_checkpoint_dir(args)
    cfg = phase2_eval_config_from_checkpoint(
        checkpoint_dir,
        env_id=str(args.env_id),
        planner=cast(PlannerName, args.planner),
        planning_samples=int(args.planning_samples),
        horizon=int(args.horizon),
        root_seed=int(args.root_seed),
        expected_checkpoint_hash=str(args.expected_checkpoint_hash),
    )
    checkpoint_ref = Phase2CheckpointRef(
        repo_id=str(args.checkpoint_repo),
        revision=str(args.checkpoint_revision),
        artifact_path=str(args.checkpoint_artifact_path),
        checkpoint_hash=str(args.expected_checkpoint_hash),
        training_job_id=str(args.training_job_id) if args.training_job_id else None,
        training_job_url=str(args.training_job_url) if args.training_job_url else None,
        code_sha=str(args.code_sha) if args.code_sha else None,
        train_config_hash=str(args.train_config_hash)
        if args.train_config_hash
        else None,
    )
    report = build_phase2_downstream_eval_report(
        checkpoint_dir,
        checkpoint_ref=checkpoint_ref,
        cfg=cfg,
        eval_command=str(args.eval_command or _canonical_command(args)),
        task_scale=str(args.task_scale),
        held_out_policy=str(args.held_out_policy),
        goal_policy=str(args.goal_policy),
        action_clipping=str(args.action_clipping),
        num_episodes=int(args.num_episodes),
        planner_iterations=int(args.planner_iters),
        published_report_uri=_report_uri(args) if args.push else None,
        source_report_uri=str(args.source_report_uri)
        if args.source_report_uri
        else None,
        claim_boundary=str(args.claim_boundary),
    )
    payload = json.dumps(report.model_dump(mode="json"), indent=2)
    parse_phase2_downstream_eval_report(json.loads(payload))
    if args.output is not None:
        args.output.parent.mkdir(parents=True, exist_ok=True)
        args.output.write_text(payload + "\n", encoding="utf-8")
    if args.push:
        _upload_report(args, payload)
    print(payload)


def _resolve_checkpoint_dir(args: argparse.Namespace) -> Path:
    if args.checkpoint_dir is not None:
        return Path(args.checkpoint_dir)
    try:
        hub = importlib.import_module("huggingface_hub")
    except ImportError as exc:  # pragma: no cover - exercised only in HF Jobs
        raise SystemExit(
            "omitting --checkpoint-dir requires huggingface_hub; run with `--with huggingface_hub`"
        ) from exc
    artifact_path = str(args.checkpoint_artifact_path).strip("/")
    hub.snapshot_download(
        repo_id=str(args.checkpoint_repo),
        repo_type="model",
        revision=str(args.checkpoint_revision),
        allow_patterns=[f"{artifact_path}/*"],
        local_dir=str(args.download_dir),
    )
    return Path(args.download_dir) / artifact_path


def _report_uri(args: argparse.Namespace) -> str:
    repo_type = str(args.out_repo_type)
    type_prefix = "models" if repo_type == "model" else "datasets"
    return f"hf://{type_prefix}/{args.out_repo}/{args.out_path}"


def _upload_report(args: argparse.Namespace, payload: str) -> None:
    try:
        hub = importlib.import_module("huggingface_hub")
    except ImportError as exc:  # pragma: no cover - exercised only with --push
        raise SystemExit(
            "--push requires huggingface_hub; run with `--with huggingface_hub`"
        ) from exc
    api = hub.HfApi()
    api.upload_file(
        path_or_fileobj=payload.encode("utf-8"),
        path_in_repo=str(args.out_path),
        repo_id=str(args.out_repo),
        repo_type=str(args.out_repo_type),
        commit_message="Add Phase 2 downstream eval report",
    )


if __name__ == "__main__":
    main()
